public class PortalMetrics {
	public double originX;
	public double originY;
	public double originZ;

	public PortalMetrics(double xCoord, double yCoord, double zCoord) {
		this.originX = xCoord;
		this.originY = yCoord;
		this.originZ = zCoord;
	}
}
